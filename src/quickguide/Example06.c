#include <stdio.h>

int main()
{
	puts("\\ character: \\");
	puts("\' character: \'");
	puts("\" character: \"");
	puts("\? character: \?");
	puts("Alert or bell: \a");
	puts("Backspace: \b");
	puts("Form feed: \f");
	puts("Newline: \n");
	puts("Carriage return: \r");
	puts("Horizontal tab: \t");
	puts("Vertical tab: \v");
	puts("Octal number of one to three digits: \0451");
	puts("Hexadecimal number of one or more digits: \x5a");
	return 0;
}